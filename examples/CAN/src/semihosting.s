#***************************************************************************************************************************

.thumb

#***************************************************************************************************************************

.syntax unified
.cpu    cortex-m4
.fpu    softvfp

#***************************************************************************************************************************

.global _putc
.global _puts

#***************************************************************************************************************************

.equ SYS_PUTC, 0x03
.equ SYS_PUTS, 0x04
.equ SEMIHOST, 0xab

#***************************************************************************************************************************

.text

#***************************************************************************************************************************

_putc:
	push {r0}

   	mov r1,sp
   	ldr r0, =SYS_PUTC
	bkpt SEMIHOST

	
	pop {r0}

	bx lr

#***************************************************************************************************************************

_puts:

   	mov r1,r0
   	ldr r0, =SYS_PUTS
	bkpt SEMIHOST

	bx lr
